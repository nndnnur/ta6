	<h2 align="center">Total 27.549 Kasus Covid-19 di Indonesia dan Kerentanan Anak Terhadap Penularan Virus</h2>
	<center><img src="img/bapa.jpeg"></center>
	<br>
	<p>Sejak diumumkan pertama kali pada 2 maret 2020, jumlah kasus positif covid-19 di Tanah Air masih terus bertambah.</p>
    <p>Berdasarkan data terbaru yang dihimpun hiingga Selasa(2/6/2020) pukul 12.00 WIB, total ada 27.549 kasus positif covid-19 di indonesia, dangan penambahan 609 pasien dalam 24 jam terakhir. "Dari hasil pemeriksaan ini konfirmasikan kasus positif 609 orang sehingga menjadi 27.549 orang", kata Juru Bicara Pemerintah untuk penanganan covid-19 Achmad Yurianto, di Graha BNPB, Jakarta Selasa sore. Selain itu, Yuri mengatakan , ada penambahan 289 pasien sembuh dari covid-19, sehingga total pasien mencapai 7.935 orang.</p>
    <p>Kemudian, terdapat penambahan pasien covid-19 yang meninggal dunia sejak 24 jam terakhir. Dengan demikian, total pasien covid-19 yang meninggal dunia hingga saat ini ada 1.663 orang. Dalam periode yang sama, jumlah orang dalam pemantauan (ODP) dan pasien dalam pengawasan (PDP) terkait covid-19 bertambah dari data sebelumnya. Yuri mengatakan, jumlah ODP di indonesia berjumlah 48.023 orang. Sementara itu, jumlah PDP sebanyak 13.213 orang.</p>
